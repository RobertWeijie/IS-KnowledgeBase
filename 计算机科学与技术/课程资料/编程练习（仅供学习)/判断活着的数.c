#include <stdio.h>

// 判断一个数是否为“活着的数”
int is_alive(int n) {
    // 如果输入的数为 1 或 3，则是“活着的数”
    if (n == 1 || n == 3) {
        return 1;
    }
    // 枚举因子 a
    for (int a = 1; a * a <= n - 2; a++) {
        // 如果 n-2 不是 a 的倍数，则跳过当前循环
        if ((n - 2) % a != 0) {
            continue;
        }
        // 计算另一个因子 b
        int b = (n - 2) / a;
        // 如果 a 不是 1 且不是“活着的数”，则跳过当前循环
        if (a != 1 && !is_alive(a)) {
            continue;
        }
        // 如果 b 不是“活着的数”，则跳过当前循环
        if (!is_alive(b)) {
            continue;
        }
        // 如果 2+ab+2a+2b 等于输入的数 n，则是“活着的数”
        if (2 + a * b + 2 * a + 2 * b == n) {
            return 1;
        }
    }
    // 所有可能的组合都不满足条件，则不是“活着的数”
    return 0;
}

// 主函数
int main() {
    int n;
    // 循环读入输入数据
    while (scanf("%d", &n) == 1) {
        // 如果输入的数是“活着的数”，输出 Yes，否则输出 No
        if (is_alive(n)) {
            printf("Yes\n");
        } else {
            printf("No\n");
        }
    }
    return 0;
}
